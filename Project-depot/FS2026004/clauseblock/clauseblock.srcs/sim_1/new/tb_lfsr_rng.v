`timescale 1ns/1ps

module tb_lfsr_rng;

//////////////////////////////////////////////////
// Inputs
//////////////////////////////////////////////////

reg r_clk;
reg r_reset;

//////////////////////////////////////////////////
// Outputs
//////////////////////////////////////////////////

wire [7:0] w_random_num;

//////////////////////////////////////////////////
// DUT
//////////////////////////////////////////////////

lfsr_rng
#(
    .SEED(8'hA5)
)
u_lfsr_rng
(
    .i_clk(r_clk),
    .i_reset(r_reset),

    .o_random_num(w_random_num)
);

//////////////////////////////////////////////////
// Clock Generation
//////////////////////////////////////////////////

always #5 r_clk = ~r_clk;

//////////////////////////////////////////////////
// Test
//////////////////////////////////////////////////

initial
begin

    r_clk   = 1'b0;
    r_reset = 1'b1;

    #20;

    r_reset = 1'b0;

    #200;

    $finish;

end

//////////////////////////////////////////////////
// Monitor
//////////////////////////////////////////////////

always @(posedge r_clk)
begin

    $display("Time=%0t Random=%3d Binary=%b",
             $time,
             w_random_num,
             w_random_num);

end

endmodule