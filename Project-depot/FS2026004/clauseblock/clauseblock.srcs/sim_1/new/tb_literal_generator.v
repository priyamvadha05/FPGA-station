    //--------------------------------------------------

    r_X = 12'b101100110101;
    #10;

    //--------------------------------------------------
    // Test 2
    //--------------------------------------------------

    r_X = 12'b000011110000;
    #10;

    //--------------------------------------------------
    // Test 3
    //--------------------------------------------------

    r_X = 12'b111111111111;
    #10;

    $finish;

end

endmodule