module tb_literal_generator;

//////////////////////////////////////////////////
// Inputs
//////////////////////////////////////////////////

reg  [11:0] r_X;

//////////////////////////////////////////////////
// Outputs
//////////////////////////////////////////////////

wire [23:0] w_literals;

//////////////////////////////////////////////////
// DUT
//////////////////////////////////////////////////

literal_generator u_literal_generator
(
    .i_X(r_X),
    .o_literals(w_literals)
);

//////////////////////////////////////////////////
// Test
//////////////////////////////////////////////////

initial
begin

    $monitor("Time=%0t X=%b Literals=%b",
             $time,
             r_X,
             w_literals);

    //--------------------------------------------------
