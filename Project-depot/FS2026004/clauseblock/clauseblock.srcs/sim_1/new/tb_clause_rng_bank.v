`timescale 1ns/1ps

module tb_clause_rng_bank;

//////////////////////////////////////////////////
// Inputs
//////////////////////////////////////////////////

reg r_clk;
reg r_reset;

//////////////////////////////////////////////////
// Outputs
//////////////////////////////////////////////////

wire [159:0] w_clause_random_bus;

//////////////////////////////////////////////////
// DUT
//////////////////////////////////////////////////

clause_rng_bank u_clause_rng_bank
(
    .i_clk(r_clk),
    .i_reset(r_reset),

    .o_clause_random_bus(w_clause_random_bus)
);

//////////////////////////////////////////////////
// Clock Generation
//////////////////////////////////////////////////

always #5 r_clk = ~r_clk;

//////////////////////////////////////////////////
// Variables
//////////////////////////////////////////////////

integer i;

//////////////////////////////////////////////////
// Test
//////////////////////////////////////////////////

initial
begin

    r_clk   = 1'b0;
    r_reset = 1'b1;

    #20;
    r_reset = 1'b0;

    #100;

    $finish;

end

//////////////////////////////////////////////////
// Monitor
//////////////////////////////////////////////////

always @(posedge r_clk)
begin

    $display("--------------------------------------");

    for(i = 0; i < 20; i = i + 1)
    begin
        $display("Clause %0d Random = %3d (%b)",
                 i,
                 w_clause_random_bus[(i*8)+:8],
                 w_clause_random_bus[(i*8)+:8]);
    end

end

endmodule